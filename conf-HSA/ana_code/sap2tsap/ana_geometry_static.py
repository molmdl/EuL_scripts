def write_pdb(atoms, xp_nm, geo, outfile, label):
    if geo["a2_sap"] <= geo["a2_tsap"]:
        ideal = geo["ideal_sap"]
        ideal_label = "SAP"
    else:
        ideal = geo["ideal_tsap"]
        ideal_label = "TSAP"

    abs_ideal = geo["eu_pos"] + ideal
    xp_A = xp_nm * 10.0

    with open(outfile, "w") as f:
        f.write(f"REMARK  label={label}\n")
        f.write(
            f"REMARK  torsion_geom={geo['geom_torsion']}  a2_geom={geo['geom_a2']}  "
            f"A_SAP={geo['rmsd_sap']:.3f}A  A_TSAP={geo['rmsd_tsap']:.3f}A  "
            f"A2_SAP={geo['a2_sap']:.3f}A  A2_TSAP={geo['a2_tsap']:.3f}A\n"
        )
        f.write(f"REMARK  Chain A = complex  |  Chain B = ideal {ideal_label} coordination geometry\n")

        for i, atom in enumerate(atoms):
            p = xp_A[i]
            name = atom["atomname"][:4].ljust(4)
            f.write(
                f"ATOM  {i+1:5d} {name} {atom['resname']:<3s} A{1:4d}    "
                f"{p[0]:8.3f}{p[1]:8.3f}{p[2]:8.3f}  1.00  0.00\n"
            )

        for j, (dname, dp) in enumerate(zip(DUM_NAMES, abs_ideal)):
            serial = len(atoms) + j + 1
            f.write(
                f"HETATM{serial:5d} {dname} DUM B{2:4d}    "
                f"{dp[0]:8.3f}{dp[1]:8.3f}{dp[2]:8.3f}  1.00  0.00\n"
            )

        f.write("END\n")

    print(f"  Wrote: {outfile}")


def _geo_block(label, geo):
    lines = []
    lines.append(f"  +-- {label}")
    lines.append(f"  |  Torsion classification       : {geo['geom_torsion']}")
    lines.append(f"  |  A2 classification            : {geo['geom_a2']}")
    lines.append(f"  |  Twist angle (TOP-BOTTOM)     : {geo['twist']:+.2f} deg")
    lines.append("  |")
    lines.append("  |  Part A (coordination-only):")
    lines.append(f"  |    RMSD to ideal SAP          = {geo['rmsd_sap']:.4f} A")
    lines.append(f"  |    RMSD to ideal TSAP         = {geo['rmsd_tsap']:.4f} A")
    lines.append("  |")
    lines.append("  |  Part A2 (pure geometry, extended):")
    lines.append(f"  |    A2_SAP (RMSD)              = {geo['a2_sap']:.4f} A")
    lines.append(f"  |    A2_TSAP (RMSD)             = {geo['a2_tsap']:.4f} A")
    lines.append(f"  |    deltaA2 (TSAP-SAP)         = {geo['delta_a2']:+.4f} A")
    lines.append(f"  |    CShM_SAP                   = {geo['cshm_sap']:.4f}")
    lines.append(f"  |    CShM_TSAP                  = {geo['cshm_tsap']:.4f}")
    lines.append("  |")
    lines.append("  |  Part B torsions:")

    mean_ring = float(np.nanmean(geo["ring_tors"]))
    for (name, *_), ang in zip(RING_TORS, geo["ring_tors"]):
        lines.append(f"  |    {name.strip():<22s}: {ang:+8.2f} deg")
    lines.append(f"  |    {'mean ring':<22s}: {mean_ring:+8.2f} deg")
    lines.append(f"  |    {CHROM_TOR[0].strip():<22s}: {geo['chrom_tor']:+8.2f} deg")
    lines.append("  +--------------------------------")
    return lines


def write_summary(geo_orig, geo_inv, stem_orig, stem_inv, outfile):
    sep = "=" * 84
    out = []

    out.append(sep)
    out.append("  ana_geometry_static.py - geometry report (A, A2-pure-geometry, B)")
    out.append(sep)
    out.append(f"  Input (original) : {stem_orig}")
    if stem_inv:
        out.append(f"  Input (inverted) : {stem_inv}")
    out.append("")

    out.append("  ORIGINAL geometry:")
    out.extend(_geo_block("original", geo_orig))
    out.append("")

    if geo_inv is not None:
        out.append("  Tc-INVERTED geometry:")
        out.extend(_geo_block("Tc-inverted", geo_inv))
        out.append("")

        out.append(f"  {'Quantity':<36s}  {'Original':>10s}  {'Inverted':>10s}  {'Delta':>10s}")
        out.append("  " + "-" * 80)

        def row(label, vo, vi, fmt="{:+8.2f}"):
            return (
                f"  {label:<36s}  {fmt.format(vo):>10s}  {fmt.format(vi):>10s}  "
                f"{fmt.format(vi - vo):>10s}"
            )

        out.append(row("Twist angle (deg)", geo_orig["twist"], geo_inv["twist"]))
        out.append(row("A RMSD to SAP (A)", geo_orig["rmsd_sap"], geo_inv["rmsd_sap"], "{:8.4f}"))
        out.append(row("A RMSD to TSAP (A)", geo_orig["rmsd_tsap"], geo_inv["rmsd_tsap"], "{:8.4f}"))
        out.append(row("A2_SAP (A)", geo_orig["a2_sap"], geo_inv["a2_sap"], "{:8.4f}"))
        out.append(row("A2_TSAP (A)", geo_orig["a2_tsap"], geo_inv["a2_tsap"], "{:8.4f}"))
        out.append(row("deltaA2 (A)", geo_orig["delta_a2"], geo_inv["delta_a2"], "{:+8.4f}"))
        out.append(row("CShM_SAP", geo_orig["cshm_sap"], geo_inv["cshm_sap"], "{:8.4f}"))
        out.append(row("CShM_TSAP", geo_orig["cshm_tsap"], geo_inv["cshm_tsap"], "{:8.4f}"))

        for (name, *_), ao, ai in zip(RING_TORS, geo_orig["ring_tors"], geo_inv["ring_tors"]):
            out.append(row(name.strip(), ao, ai))

        out.append(
            row(
                "mean ring torsion (deg)",
                float(np.nanmean(geo_orig["ring_tors"])),
                float(np.nanmean(geo_inv["ring_tors"])),
            )
        )
        out.append(row(CHROM_TOR[0].strip(), geo_orig["chrom_tor"], geo_inv["chrom_tor"]))

        out.append("  " + "-" * 80)
        out.append(f"  Torsion class: {geo_orig['geom_torsion']} -> {geo_inv['geom_torsion']}")
        out.append(f"  A2 class     : {geo_orig['geom_a2']} -> {geo_inv['geom_a2']}")

    out.append("")
    out.append(sep)

    text = "\n".join(out) + "\n"
    with open(outfile, "w") as fh:
        fh.write(text)

    print(f"  Wrote: {outfile}")
    print()
    print(text)

