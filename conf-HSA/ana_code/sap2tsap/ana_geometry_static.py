def invert_tc_gro(xp):
    xp = xp.copy()

    i_n4 = TC_N4_IDX - 1
    i_c17 = TC_C17_IDX - 1
    i_c24 = TC_C24_IDX - 1
    i_n8 = TC_N8_IDX - 1
    i_h45 = TC_H45_IDX - 1
    i_h46 = TC_H46_IDX - 1

    p_n4 = xp[i_n4].copy()
    p_c24 = xp[i_c24].copy()
    p_n8 = xp[i_n8].copy()

    plane_n = np.cross(p_c24 - p_n4, p_n8 - p_n4)
    pn = np.linalg.norm(plane_n)
    if pn < 1e-9:
        sys.exit("ERROR: N4, C24, N8 are collinear - cannot define reflection plane.")
    plane_n /= pn

    for idx in (i_c17, i_h45, i_h46):
        rel = xp[idx] - p_n4
        xp[idx] = xp[idx] - 2.0 * np.dot(rel, plane_n) * plane_n

    return xp

