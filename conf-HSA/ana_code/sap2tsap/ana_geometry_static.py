def dihedral_deg(p1, p2, p3, p4):
    b1 = p2 - p1
    b2 = p3 - p2
    b3 = p4 - p3
    n1 = np.cross(b1, b2)
    n2 = np.cross(b2, b3)
    nn1 = np.linalg.norm(n1)
    nn2 = np.linalg.norm(n2)
    if nn1 < 1e-9 or nn2 < 1e-9:
        return np.nan
    n1 /= nn1
    n2 /= nn2
    b2n = b2 / np.linalg.norm(b2)
    return float(np.degrees(np.arctan2(np.dot(np.cross(n1, n2), b2n), np.dot(n1, n2))))


def _kabsch_batch(Ps, Q):
    H = np.einsum("kni,nj->kij", Ps, Q)
    U, _, Vt = np.linalg.svd(H)

    dets = np.linalg.det(np.einsum("kij,kjl->kil", Vt.swapaxes(1, 2), U.swapaxes(1, 2)))
    signs = np.sign(dets)

    diag = np.zeros((len(Ps), 3, 3))
    diag[:, 0, 0] = 1.0
    diag[:, 1, 1] = 1.0
    diag[:, 2, 2] = signs

    Rs = np.einsum("kij,kjl,klm->kim", Vt.swapaxes(1, 2), diag, U.swapaxes(1, 2))
    rotated = np.einsum("kni,kji->knj", Ps, Rs)

    diff = rotated - Q[np.newaxis]
    rmsds = np.sqrt(np.mean(np.sum(diff * diff, axis=2), axis=1))
    return rmsds, Rs


def kabsch(P, Q):
    H = P.T @ Q
    U, _, Vt = np.linalg.svd(H)
    d = float(np.sign(np.linalg.det(Vt.T @ U.T)))
    R = Vt.T @ np.diag([1.0, 1.0, d]) @ U.T
    diff = P @ R.T - Q
    rmsd = float(np.sqrt(np.mean(np.einsum("ij,ij->i", diff, diff))))
    return rmsd, R


def procrustes_scale_rotate(source, target):
    """Fit target ~= s * source @ R.T ; source/target assumed centered."""
    _, R = kabsch(source, target)
    src_rot = source @ R.T
    denom = float(np.sum(src_rot * src_rot))
    if denom < 1e-12:
        s = 1.0
    else:
        s = float(np.sum(src_rot * target) / denom)
    return s, R


def get_c4_frame(coord9):
    ring_n = coord9[BOTTOM]
    centroid = ring_n.mean(0)
    _, _, vt = np.linalg.svd(ring_n - centroid)
    z = vt[-1]
    if np.dot(z, coord9[CAP[0]]) < 0:
        z = -z

    cap_vec = coord9[CAP[0]]
    cap_proj = cap_vec - np.dot(cap_vec, z) * z
    cap_norm = np.linalg.norm(cap_proj)
    if cap_norm > 1e-8:
        x = cap_proj / cap_norm
    else:
        tmp = np.array([1.0, 0.0, 0.0]) if abs(z[0]) < 0.9 else np.array([0.0, 1.0, 0.0])
        x = np.cross(z, tmp)
        x /= np.linalg.norm(x)

    y = np.cross(z, x)
    y /= np.linalg.norm(y)
    return x, y, z


def _azimuths(vecs, x, y, z):
    vp = vecs - (vecs @ z)[:, np.newaxis] * z
    return np.degrees(np.arctan2(vp @ y, vp @ x))


def get_twist_angle(coord9):
    x, y, z = get_c4_frame(coord9)
    phi_bot = np.sort(_azimuths(coord9[BOTTOM], x, y, z))
    phi_top = np.sort(_azimuths(coord9[TOP], x, y, z))
    offsets = (phi_top - phi_bot + 90.0) % 180.0 - 90.0
    return float(offsets.mean())


def build_local_ideal_directions(coord9):
    x, y, z = get_c4_frame(coord9)

    zh = coord9 @ z
    vp = coord9 - zh[:, np.newaxis] * z
    radii = np.linalg.norm(vp, axis=1)

    r_bot = float(radii[BOTTOM].mean())
    z_bot = float(zh[BOTTOM].mean())
    r_top = float(radii[TOP].mean())
    z_top = float(zh[TOP].mean())
    r_cap = float(radii[CAP[0]])
    z_cap = float(zh[CAP[0]])

    def make_dirs(twist_deg):
        out = np.zeros((9, 3))
        phi_b = np.radians(np.arange(4) * 90.0)
        phi_t = phi_b + np.radians(twist_deg)

        out[BOTTOM] = (
            r_bot * np.cos(phi_b)[:, np.newaxis] * x[np.newaxis, :]
            + r_bot * np.sin(phi_b)[:, np.newaxis] * y[np.newaxis, :]
            + z_bot * z[np.newaxis, :]
        )
        out[TOP] = (
            r_top * np.cos(phi_t)[:, np.newaxis] * x[np.newaxis, :]
            + r_top * np.sin(phi_t)[:, np.newaxis] * y[np.newaxis, :]
            + z_top * z[np.newaxis, :]
        )
        out[CAP[0]] = r_cap * x + z_cap * z

        norms = np.linalg.norm(out, axis=1, keepdims=True)
        norms = np.where(norms > 1e-12, norms, 1.0)
        return out / norms

    return make_dirs(SAP_TWIST_DEG), make_dirs(TSAP_TWIST_DEG)


CYCLIC_PERMS = []
for db in range(4):
    for dt in range(4):
        p = list(range(9))
        for ii, orig in enumerate(BOTTOM):
            p[orig] = BOTTOM[(ii + db) % 4]
        for ii, orig in enumerate(TOP):
            p[orig] = TOP[(ii + dt) % 4]
        CYCLIC_PERMS.append(p)
CYCLIC_PERMS = np.array(CYCLIC_PERMS, dtype=int)


def min_rmsd_cyclic(coord9, dirs9, return_transform=False):
    real_r = np.linalg.norm(coord9, axis=1)
    q_unit = coord9 / np.where(real_r > 1e-12, real_r, 1.0)[:, np.newaxis]

    ps_unit = dirs9[CYCLIC_PERMS]
    _, rs = _kabsch_batch(ps_unit, q_unit)

    rotated_unit = np.einsum("kni,kji->knj", ps_unit, rs)
    ideal_placed = rotated_unit * real_r[np.newaxis, :, np.newaxis]

    diff = ideal_placed - coord9[np.newaxis]
    rmsds = np.sqrt(np.mean(np.sum(diff * diff, axis=2), axis=1))

    best_k = int(np.argmin(rmsds))
    best_rmsd = float(rmsds[best_k])

    if return_transform:
        return best_rmsd, ideal_placed[best_k]
    return best_rmsd


def classify_geom(ring_tors, chrom_tor, thresh=CLASSIFY_THRESH):
    ring_arr = np.asarray(ring_tors, dtype=float)
    valid = ring_arr[~np.isnan(ring_arr)]
    if len(valid) == 0 or np.isnan(chrom_tor):
        return "UNK"
    mean_ring = float(valid.mean())
    if abs(mean_ring) < thresh or abs(chrom_tor) < thresh:
        return "UNK"
    return "SAP" if np.sign(mean_ring) != np.sign(chrom_tor) else "TSAP"

