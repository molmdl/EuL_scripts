def extract_geometry(xp_nm, ref_a2):
    eu_nm = xp_nm[METAL_GRO - 1]
    coord9_nm = xp_nm[[g - 1 for g in COORD_GRO]] - eu_nm

    coord9_A = coord9_nm * 10.0
    eu_A = eu_nm * 10.0
    ext_obs_A = (xp_nm[[g - 1 for g in A2_EXT_GRO]] - eu_nm) * 10.0

    twist = get_twist_angle(coord9_A)
    dirs_sap, dirs_tsap = build_local_ideal_directions(coord9_A)
    rmsd_sap, ideal_sap = min_rmsd_cyclic(coord9_A, dirs_sap, return_transform=True)
    rmsd_tsap, ideal_tsap = min_rmsd_cyclic(coord9_A, dirs_tsap, return_transform=True)

    ring_tors = []
    for _, i, j, k, l in RING_TORS:
        ring_tors.append(dihedral_deg(xp_nm[i - 1], xp_nm[j - 1], xp_nm[k - 1], xp_nm[l - 1]))
    _, i, j, k, l = CHROM_TOR
    tc = dihedral_deg(xp_nm[i - 1], xp_nm[j - 1], xp_nm[k - 1], xp_nm[l - 1])

    geom_torsion = classify_geom(ring_tors, tc)
    a2 = compute_a2_scores(coord9_A, ideal_sap, ideal_tsap, ext_obs_A, ref_a2)

    return {
        "eu_pos": eu_A,
        "coord9": coord9_A,
        "twist": twist,
        "rmsd_sap": float(rmsd_sap),
        "rmsd_tsap": float(rmsd_tsap),
        "ideal_sap": ideal_sap,
        "ideal_tsap": ideal_tsap,
        "ring_tors": ring_tors,
        "chrom_tor": tc,
        "geom_torsion": geom_torsion,
        **a2,
    }

