def parse_args():
    p = argparse.ArgumentParser(description="Static single-frame geometry analysis (A, A2, B)")
    p.add_argument("--gro", required=True, metavar="FILE", help="Original GRO file")
    p.add_argument("--gro-inv", default=None, metavar="FILE", help="Pre-inverted GRO file")
    p.add_argument("--invert-tc", action="store_true", help="Generate Tc-inverted coordinates in memory")
    p.add_argument("--out-dir", default=None, metavar="DIR", help="Output directory")
    return p.parse_args()


def main():
    args = parse_args()

    if not os.path.isfile(args.gro):
        sys.exit(f"ERROR: GRO file not found: {args.gro}")

    out_dir = args.out_dir or os.path.dirname(os.path.abspath(args.gro))
    os.makedirs(out_dir, exist_ok=True)

    stem = os.path.splitext(os.path.basename(args.gro))[0]

    print(f"\nLoading: {args.gro}")
    _, atoms, _ = read_gro(args.gro)
    xp_orig = atoms_to_xp(atoms)
    print(f"  Atoms: {len(atoms)}")

    # A2 reference is fixed from ORIGINAL structure for this run
    ref_a2 = make_a2_reference(xp_orig)

    print("  Analyzing original geometry ...")
    geo_orig = extract_geometry(xp_orig, ref_a2)
    write_pdb(atoms, xp_orig, geo_orig, os.path.join(out_dir, f"{stem}_geo_orig.pdb"), f"{stem}_original")

    geo_inv = None
    stem_inv = None
    atoms_inv = atoms

    if args.gro_inv:
        if not os.path.isfile(args.gro_inv):
            sys.exit(f"ERROR: Inverted GRO file not found: {args.gro_inv}")
        print(f"\nLoading inverted GRO: {args.gro_inv}")
        _, atoms_inv, _ = read_gro(args.gro_inv)
        if len(atoms_inv) != len(atoms):
            sys.exit("ERROR: Atom count mismatch between original and inverted GRO files.")
        xp_inv = atoms_to_xp(atoms_inv)
        stem_inv = args.gro_inv
    elif args.invert_tc:
        print("\nGenerating Tc-inverted coordinates in memory ...")
        xp_inv = invert_tc_gro(xp_orig)
        atoms_inv = [dict(a) for a in atoms]
        stem_inv = f"{stem} (Tc-inverted in memory)"

    if stem_inv is not None:
        print("  Analyzing inverted geometry ...")
        geo_inv = extract_geometry(xp_inv, ref_a2)
        write_pdb(
            atoms_inv,
            xp_inv,
            geo_inv,
            os.path.join(out_dir, f"{stem}_geo_inv.pdb"),
            f"{stem}_tc_inverted",
        )

    write_summary(
        geo_orig,
        geo_inv,
        args.gro,
        stem_inv,
        os.path.join(out_dir, f"{stem}_geo_summary.txt"),
    )

    print("Done.")


if __name__ == "__main__":
    main()
