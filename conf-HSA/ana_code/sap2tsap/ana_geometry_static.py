#!/usr/bin/env python3
"""
ana_geometry_static.py
----------------------
Static geometry analysis for 9-coordinate Eu complexes from GRO files.

Part A:
  Chimera-style coordination-sphere RMSD to ideal SAP/TSAP.

Part A2:
  Purely geometry-based extended score using 11 points:
    - 9 coordination atoms
    - 2 Tc-link atoms (C17, C24)
  The 2 extra ideal points are generated by geometric projection from a fixed
  reference structure using class-specific Procrustes transforms derived from
  the 9-point ideal fit. No torsion sign rule and no additive penalty are used.

Part B:
  Torsion analysis (T1-T4 and Tc) with sign-based SAP/TSAP classification.
"""

import argparse
import os
import sys

import numpy as np

# =============================================================================
# Configuration
# =============================================================================

METAL_GRO = 55
COORD_GRO = [1, 2, 3, 4, 5, 6, 7, 8, 64]
BOTTOM = [3, 4, 5, 6]
TOP = [0, 1, 2, 7]
CAP = [8]

SAP_TWIST_DEG = -45.0
TSAP_TWIST_DEG = 0.0

RING_TORS = [
    ("T1  N4 -C9 -C10-N5 ", 4, 9, 10, 5),
    ("T2  N5 -C11-C12-N6 ", 5, 11, 12, 6),
    ("T3  N6 -C13-C14-N7 ", 6, 13, 14, 7),
    ("T4  N7 -C15-C16-N4 ", 7, 15, 16, 4),
]
CHROM_TOR = ("Tc  N4 -C17-C24-N8 ", 4, 17, 24, 8)

CLASSIFY_THRESH = 10.0
A2_AMBIG_THRESH_A = 0.05

# Tc inversion helper indices
TC_N4_IDX = 4
TC_C17_IDX = 17
TC_C24_IDX = 24
TC_N8_IDX = 8
TC_H45_IDX = 45
TC_H46_IDX = 46

# Extra atoms added in A2
A2_EXT_GRO = [TC_C17_IDX, TC_C24_IDX]

DUM_NAMES = ["DO1 ", "DO2 ", "DO3 ", "DN4 ", "DN5 ", "DN6 ", "DN7 ", "DN8 ", "DN64"]

