# A2: pure geometry extended score
# =============================================================================


def cshm_score(real_xyz, ideal_xyz):
    diff2 = np.sum((real_xyz - ideal_xyz) ** 2)
    cen = real_xyz.mean(axis=0)
    denom = np.sum((real_xyz - cen) ** 2)
    if denom < 1e-12:
        return 0.0
    return float(100.0 * diff2 / denom)


def classify_a2(a2_sap, a2_tsap, ambig=A2_AMBIG_THRESH_A):
    d = a2_tsap - a2_sap
    if abs(d) < ambig:
        return "AMB"
    return "SAP" if d > 0.0 else "TSAP"


def make_a2_reference(xp_nm):
    eu = xp_nm[METAL_GRO - 1]
    ref_coord9_A = (xp_nm[[g - 1 for g in COORD_GRO]] - eu) * 10.0
    ref_ext_A = (xp_nm[[g - 1 for g in A2_EXT_GRO]] - eu) * 10.0
    return {"coord9_A": ref_coord9_A, "ext_A": ref_ext_A}


def compute_a2_scores(coord9_A, ideal_sap, ideal_tsap, ext_obs_A, ref_a2):
    def eval_class(ideal9):
        s, R = procrustes_scale_rotate(ref_a2["coord9_A"], ideal9)
        ext_pred = s * (ref_a2["ext_A"] @ R.T)

        real_ext = np.vstack([coord9_A, ext_obs_A])
        ideal_ext = np.vstack([ideal9, ext_pred])

        a2 = float(np.sqrt(np.mean(np.sum((real_ext - ideal_ext) ** 2, axis=1))))
        cshm = cshm_score(real_ext, ideal_ext)
        return a2, cshm

    a2_sap, cshm_sap = eval_class(ideal_sap)
    a2_tsap, cshm_tsap = eval_class(ideal_tsap)

    return {
        "a2_sap": a2_sap,
        "a2_tsap": a2_tsap,
        "delta_a2": a2_tsap - a2_sap,
        "cshm_sap": cshm_sap,
        "cshm_tsap": cshm_tsap,
        "geom_a2": classify_a2(a2_sap, a2_tsap),
    }

