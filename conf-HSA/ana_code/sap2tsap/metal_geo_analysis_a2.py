"""
metal_geo_analysis_a2.py
------------------------
Trajectory analysis for 9-coordinate Eu complexes.

Part A:
  Chimera-style coordination-sphere RMSD to ideal SAP/TSAP.

Part A2:
  Purely geometry-based extended score (11 points):
    - 9 coordination atoms
    - + 2 Tc-link atoms (C16, C23)
  The 2 extra ideal points are generated by class-specific Procrustes mapping
  from a fixed reference structure, without torsion sign rules and without
  additive penalties.

Part B:
  Torsion analysis (T1-T4 and Tc) with sign-based classification.
"""

