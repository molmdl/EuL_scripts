_TORSION_IJKL = np.array(
    [[i, j, k, l] for _, i, j, k, l in RING_TORS] + [[CHROM_TOR[1], CHROM_TOR[2], CHROM_TOR[3], CHROM_TOR[4]]],
    dtype=int,
)


def collect_trajectory_data(u, mol_ag, ref_pos, hmask, ref_a2):
    records = []

    for ts in u.trajectory:
        make_mol_whole(u, mol_ag)
        raw_pos = mol_ag.positions.copy()
        aln_pos = align_pos(raw_pos, ref_pos, hmask)

        eu = aln_pos[METAL_IDX]
        coord = aln_pos[COORD_IDX] - eu
        ext_obs = aln_pos[A2_EXT_IDX] - eu

        dirs_sap, dirs_tsap = build_local_ideal_directions(coord)
        rmsd_sap, ideal_sap = min_rmsd_cyclic(coord, dirs_sap, return_transform=True)
        rmsd_tsap, ideal_tsap = min_rmsd_cyclic(coord, dirs_tsap, return_transform=True)

        tors = calc_dihedrals_batch(raw_pos, _TORSION_IJKL)
        ring_t = list(tors[:4])
        tc = float(tors[4])
        geom_torsion = classify_geom(ring_t, tc)

        a2 = compute_a2(coord, ideal_sap, ideal_tsap, ext_obs, ref_a2)

        records.append(
            {
                "frame": int(ts.frame),
                "time": float(ts.time),
                "pos": aln_pos.copy(),
                "coord9": coord.copy(),
                "eu": eu.copy(),
                "rmsd_sap": float(rmsd_sap),
                "rmsd_tsap": float(rmsd_tsap),
                "ideal_sap": ideal_sap,
                "ideal_tsap": ideal_tsap,
                "ring_tors": ring_t,
                "chrom_tor": tc,
                "geom_torsion": geom_torsion,
                **a2,
            }
        )

    return records

