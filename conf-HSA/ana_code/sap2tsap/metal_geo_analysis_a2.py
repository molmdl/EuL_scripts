def write_pdb_with_dummies(mol_atoms, pos, ideal_placed, eu_pos, outfile, frame_idx, label):
    def elem_symbol(atype):
        t = atype.lower()
        if t.startswith("eu") or t.startswith("e"):
            return "Eu"
        if t.startswith("c"):
            return "C"
        if t.startswith("n"):
            return "N"
        if t.startswith("o"):
            return "O"
        if t.startswith("h"):
            return "H"
        return atype[:1].upper()

    with open(outfile, "w") as f:
        f.write(f"REMARK  frame={frame_idx}  label={label}\n")
        f.write("REMARK  Chain A = complex  |  Chain B = ideal coordination polyhedron\n")

        for i, atom in enumerate(mol_atoms):
            p = pos[i]
            name = atom.name[:4].ljust(4)
            elem = elem_symbol(atom.type)
            f.write(
                f"ATOM  {i+1:5d} {name} {atom.resname:<3s} A{1:4d}    "
                f"{p[0]:8.3f}{p[1]:8.3f}{p[2]:8.3f}  1.00  0.00"
                f"          {elem:>2s}\n"
            )

        abs_ideal = eu_pos + ideal_placed
        dnames = ["DO0 ", "DO1 ", "DO2 ", "DN3 ", "DN4 ", "DN5 ", "DN6 ", "DN7 ", "DN63"]
        for j, (dname, dp) in enumerate(zip(dnames, abs_ideal)):
            serial = len(mol_atoms) + j + 1
            f.write(
                f"HETATM{serial:5d} {dname} DUM B{2:4d}    "
                f"{dp[0]:8.3f}{dp[1]:8.3f}{dp[2]:8.3f}  1.00  0.00           X\n"
            )

        f.write("END\n")


def run_part_A_A2(records, mol_atoms, outdir, sysname):
    os.makedirs(outdir, exist_ok=True)

    times = np.array([r["time"] for r in records])
    a_sap = np.array([r["rmsd_sap"] for r in records])
    a_tsap = np.array([r["rmsd_tsap"] for r in records])
    a2_sap = np.array([r["a2_sap"] for r in records])
    a2_tsap = np.array([r["a2_tsap"] for r in records])
    da2 = np.array([r["delta_a2"] for r in records])
    cshm_sap = np.array([r["cshm_sap"] for r in records])
    cshm_tsap = np.array([r["cshm_tsap"] for r in records])

    csv_path = os.path.join(outdir, "A_A2_timeseries.csv")
    with open(csv_path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow([
            "frame", "time_ps", "A_SAP_A", "A_TSAP_A",
            "A2_SAP_A", "A2_TSAP_A", "deltaA2_A",
            "CShM_SAP", "CShM_TSAP", "A2_class",
        ])
        for r in records:
            w.writerow([
                r["frame"], f"{r['time']:.1f}",
                f"{r['rmsd_sap']:.4f}", f"{r['rmsd_tsap']:.4f}",
                f"{r['a2_sap']:.4f}", f"{r['a2_tsap']:.4f}", f"{r['delta_a2']:.4f}",
                f"{r['cshm_sap']:.4f}", f"{r['cshm_tsap']:.4f}", r["geom_a2"],
            ])

    tns = times / 1000.0
    fig, ax = plt.subplots(figsize=(10, 4.2))
    ax.plot(tns, a_sap, color=C_SAP, lw=1.3, alpha=0.85, label="A -> SAP")
    ax.plot(tns, a_tsap, color=C_TSAP, lw=1.3, alpha=0.85, label="A -> TSAP")
    ax.plot(tns, a2_sap, color=C_SAP, lw=1.1, ls="--", alpha=0.95, label="A2 -> SAP")
    ax.plot(tns, a2_tsap, color=C_TSAP, lw=1.1, ls="--", alpha=0.95, label="A2 -> TSAP")
    ax.set_xlabel("Time (ns)")
    ax.set_ylabel("RMSD / score (A)")
    ax.set_title(f"{sysname} - Part A and A2")
    ax.legend(framealpha=0.85, ncol=2)
    plt.tight_layout()
    fig.savefig(os.path.join(outdir, "A_A2_timeseries.png"), dpi=150)
    plt.close(fig)

    fig, ax = plt.subplots(figsize=(8, 4.2))
    bins = 60
    cnt_as, edges = np.histogram(a_sap, bins=bins)
    ctr = 0.5 * (edges[:-1] + edges[1:])
    cnt_at, _ = np.histogram(a_tsap, bins=edges)
    cnt_a2s, _ = np.histogram(a2_sap, bins=edges)
    cnt_a2t, _ = np.histogram(a2_tsap, bins=edges)
    n = max(len(records), 1)

    ax.plot(ctr, cnt_as / n, color=C_SAP, lw=1.8, label="A -> SAP")
    ax.plot(ctr, cnt_at / n, color=C_TSAP, lw=1.8, label="A -> TSAP")
    ax.plot(ctr, cnt_a2s / n, color=C_SAP, lw=1.2, ls="--", label="A2 -> SAP")
    ax.plot(ctr, cnt_a2t / n, color=C_TSAP, lw=1.2, ls="--", label="A2 -> TSAP")
    ax.set_xlabel("RMSD / score (A)")
    ax.set_ylabel("Fraction of frames")
    ax.set_title(f"{sysname} - A/A2 distributions")
    ax.legend(framealpha=0.85, ncol=2)
    plt.tight_layout()
    fig.savefig(os.path.join(outdir, "A_A2_histogram.png"), dpi=150)
    plt.close(fig)

    fi_sap = int(np.argmin(a2_sap))
    fi_tsap = int(np.argmin(a2_tsap))

    rs = records[fi_sap]
    write_pdb_with_dummies(
        mol_atoms, rs["pos"], rs["ideal_sap"], rs["eu"],
        os.path.join(outdir, f"A2_rep_SAP_{fi_sap:04d}.pdb"), fi_sap,
        f"A2_min_SAP_frame{fi_sap}",
    )

    rt = records[fi_tsap]
    write_pdb_with_dummies(
        mol_atoms, rt["pos"], rt["ideal_tsap"], rt["eu"],
        os.path.join(outdir, f"A2_rep_TSAP_{fi_tsap:04d}.pdb"), fi_tsap,
        f"A2_min_TSAP_frame{fi_tsap}",
    )

    return {
        "times": times,
        "a_sap": a_sap,
        "a_tsap": a_tsap,
        "a2_sap": a2_sap,
        "a2_tsap": a2_tsap,
        "delta_a2": da2,
        "cshm_sap": cshm_sap,
        "cshm_tsap": cshm_tsap,
    }


def run_part_B(records, outdir, sysname):
    os.makedirs(outdir, exist_ok=True)

    times = np.array([r["time"] for r in records])
    tnames = [n for n, *_ in RING_TORS] + [CHROM_TOR[0]]
    tor_mat = np.array([r["ring_tors"] + [r["chrom_tor"]] for r in records]).T

    ts_csv = os.path.join(outdir, "B_torsions_timeseries.csv")
    with open(ts_csv, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["frame", "time_ps"] + tnames + ["geom_torsion", "geom_a2"])
        for i, r in enumerate(records):
            vals = [f"{v:.2f}" for v in tor_mat[:, i]]
            w.writerow([r["frame"], f"{r['time']:.1f}"] + vals + [r["geom_torsion"], r["geom_a2"]])

    fig, ax = plt.subplots(figsize=(11, 4.5))
    for name, col, vals in zip(tnames, TOR_COLS, tor_mat):
        ax.plot(times / 1000.0, vals, color=col, lw=1.3, alpha=0.85, label=name)
    ax.axhline(0, color="k", lw=0.6, ls="--", alpha=0.5)
    ax.set_xlabel("Time (ns)")
    ax.set_ylabel("Torsion (deg)")
    ax.set_ylim(-185, 185)
    ax.set_yticks([-180, -90, 0, 90, 180])
    ax.legend(fontsize=8, ncol=len(tnames), framealpha=0.85)
    ax.set_title(f"{sysname} - Part B torsions")
    plt.tight_layout()
    fig.savefig(os.path.join(outdir, "B_torsions_timeseries.png"), dpi=150)
    plt.close(fig)

