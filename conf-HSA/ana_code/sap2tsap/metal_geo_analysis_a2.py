def write_summary(records, a_stats, outdir, sysname):
    geoms_t = [r["geom_torsion"] for r in records]
    geoms_a2 = [r["geom_a2"] for r in records]
    nf = len(records)

    def frac(arr, label):
        n = arr.count(label)
        return n, (100.0 * n / nf if nf else 0.0)

    n_sap_t, p_sap_t = frac(geoms_t, "SAP")
    n_tsap_t, p_tsap_t = frac(geoms_t, "TSAP")
    n_unk_t, p_unk_t = frac(geoms_t, "UNK")

    n_sap_a2, p_sap_a2 = frac(geoms_a2, "SAP")
    n_tsap_a2, p_tsap_a2 = frac(geoms_a2, "TSAP")
    n_amb_a2, p_amb_a2 = frac(geoms_a2, "AMB")

    out = os.path.join(outdir, "SUMMARY_A2.txt")
    with open(out, "w") as f:
        f.write(f"System: {sysname}\n")
        f.write(f"Frames: {nf}\n\n")
        f.write("Part A (coordination-only)\n")
        f.write(f"  RMSD->SAP  mean +/- std: {a_stats['a_sap'].mean():.4f} +/- {a_stats['a_sap'].std():.4f} A\n")
        f.write(f"  RMSD->TSAP mean +/- std: {a_stats['a_tsap'].mean():.4f} +/- {a_stats['a_tsap'].std():.4f} A\n\n")

        f.write("Part A2 (pure geometry, extended set)\n")
        f.write(f"  A2->SAP    mean +/- std: {a_stats['a2_sap'].mean():.4f} +/- {a_stats['a2_sap'].std():.4f} A\n")
        f.write(f"  A2->TSAP   mean +/- std: {a_stats['a2_tsap'].mean():.4f} +/- {a_stats['a2_tsap'].std():.4f} A\n")
        f.write(f"  deltaA2    mean +/- std: {a_stats['delta_a2'].mean():+.4f} +/- {a_stats['delta_a2'].std():.4f} A\n")
        f.write(f"  CShM_SAP   mean +/- std: {a_stats['cshm_sap'].mean():.4f} +/- {a_stats['cshm_sap'].std():.4f}\n")
        f.write(f"  CShM_TSAP  mean +/- std: {a_stats['cshm_tsap'].mean():.4f} +/- {a_stats['cshm_tsap'].std():.4f}\n")
        f.write(
            f"  A2 class fractions: SAP={n_sap_a2} ({p_sap_a2:.1f}%), "
            f"TSAP={n_tsap_a2} ({p_tsap_a2:.1f}%), "
            f"AMB={n_amb_a2} ({p_amb_a2:.1f}%)\n\n"
        )

        f.write("Part B torsion class fractions\n")
        f.write(
            f"  SAP={n_sap_t} ({p_sap_t:.1f}%), "
            f"TSAP={n_tsap_t} ({p_tsap_t:.1f}%), "
            f"UNK={n_unk_t} ({p_unk_t:.1f}%)\n"
        )

    return out


def parse_args():
    p = argparse.ArgumentParser(
        description="Trajectory analysis with Part A, pure-geometry A2, and Part B",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=textwrap.dedent(
            """
            Example:
              python metal_geo_analysis_a2.py \\
                --system me_sssL_sap --tpr me_sssL_sap/pr_0.tpr --xtc me_sssL_sap/pr_0.xtc
            """
        ),
    )

    p.add_argument("--system", dest="names", action="append", metavar="NAME")
    p.add_argument("--tpr", dest="tprs", action="append", metavar="FILE")
    p.add_argument("--xtc", dest="xtcs", action="append", metavar="FILE")
    p.add_argument("--outdir", default="analysis_a2", metavar="DIR")

    args = p.parse_args()
    names = args.names or []
    tprs = args.tprs or []
    xtcs = args.xtcs or []

    if not names:
        p.error("No systems specified. Use repeated --system/--tpr/--xtc triplets.")
    if not (len(names) == len(tprs) == len(xtcs)):
        p.error("Mismatch among --system, --tpr, and --xtc counts.")

    systems = {}
    for n, tpr, xtc in zip(names, tprs, xtcs):
        if n in systems:
            p.error(f"Duplicate system name: {n}")
        if not os.path.isfile(tpr):
            p.error(f"TPR not found: {tpr}")
        if not os.path.isfile(xtc):
            p.error(f"XTC not found: {xtc}")
        systems[n] = {"tpr": tpr, "xtc": xtc, "outdir": os.path.join(n, args.outdir)}

    return systems


def main():
    systems = parse_args()

    for sysname, cfg in systems.items():
        print("\n" + "=" * 70)
        print(f"System: {sysname}")
        print("=" * 70)

        u, mol_ag = load_mol_universe(cfg["tpr"], cfg["xtc"])
        print(f"MOL atoms: {mol_ag.n_atoms}   Frames: {u.trajectory.n_frames}")

        u.trajectory[0]
        make_mol_whole(u, mol_ag)
        ref_pos = mol_ag.positions.copy()
        hmask = heavy_mask(mol_ag)
        ref_a2 = make_a2_reference(ref_pos)

        print("Collecting trajectory metrics ...")
        records = collect_trajectory_data(u, mol_ag, ref_pos, hmask, ref_a2)
        print(f"Collected {len(records)} frames")

        outdir = cfg["outdir"]
        os.makedirs(outdir, exist_ok=True)

        print("Writing Part A/A2 outputs ...")
        a_stats = run_part_A_A2(records, mol_ag, outdir, sysname)

        print("Writing Part B outputs ...")
        run_part_B(records, outdir, sysname)

        summary = write_summary(records, a_stats, outdir, sysname)

        geoms_t = [r["geom_torsion"] for r in records]
        geoms_a2 = [r["geom_a2"] for r in records]
        nf = len(records)

        def pct(n):
            return 100.0 * n / nf if nf else 0.0

        print("Summary:")
        print(
            f"  Torsion class: SAP={geoms_t.count('SAP')} ({pct(geoms_t.count('SAP')):.1f}%), "
            f"TSAP={geoms_t.count('TSAP')} ({pct(geoms_t.count('TSAP')):.1f}%), "
            f"UNK={geoms_t.count('UNK')} ({pct(geoms_t.count('UNK')):.1f}%)"
        )
        print(
            f"  A2 class     : SAP={geoms_a2.count('SAP')} ({pct(geoms_a2.count('SAP')):.1f}%), "
            f"TSAP={geoms_a2.count('TSAP')} ({pct(geoms_a2.count('TSAP')):.1f}%), "
            f"AMB={geoms_a2.count('AMB')} ({pct(geoms_a2.count('AMB')):.1f}%)"
        )
        print(f"  A  mean SAP/TSAP  : {a_stats['a_sap'].mean():.3f} / {a_stats['a_tsap'].mean():.3f} A")
        print(f"  A2 mean SAP/TSAP  : {a_stats['a2_sap'].mean():.3f} / {a_stats['a2_tsap'].mean():.3f} A")
        print(f"  Summary file: {summary}")

    print("\nAll done.")


if __name__ == "__main__":
    main()
